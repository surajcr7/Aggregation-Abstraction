package abstraction;

public class Abstraction {
	/*
	 * ============ Abstraction =========
	 * # Process of hiding implementation details.
	 * 
	 * # To achieve abstraction :
	 * 
	 * 		1> Abstract class :
	 * 			# Class which contains abstract methods (unimplemented method) and non- abstract methods (implemented methods)is known as abstraction.
	 * 				#Abstract Method : 
	 * 					-> Methods which does not have body/ implementation details.
	 * 					-> e.g.
	 * 						abstract void sum();
	 * 						abstract int printValue(int x);
	 * 
	 * 			# We can't create object of abstract class.
	 * 			# Every abstract class must have child class and child class must override abstract method.
	 * 			
	 * 			# Level of abstraction 0-100%
	 * 					
	 * 				syntax:
	 * 					abstract class class_name{
	 * 			
	 * 						//abstract methods
	 * 
	 * 						//non-abstract methods
	 * 
	 * 
	 *              }
	 * 
	 * 	    2> Interface :
	 * 		
	 * 		# same as class which contains public abstract methods and public static final variables.
	 *      # We can't create object of interface.
	 *      # Every interface must have at least one implementation class and class must override 
	 *      # 100% abstraction level.
	 *      
	 *      		syntax :
	 *      				interface interface_name {
	 *      
	 *      					
	 *      					// public abstract methods
	 *      
	 *      
	 *      
	 * 
	 */

}
